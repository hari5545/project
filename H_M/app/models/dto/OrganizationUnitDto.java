package models.dto;

public class OrganizationUnitDto {
		
	protected String orgUnitName;
	protected String orgName;
	protected String orgUnitLocation;
	protected String orgUnitLatitude;
	protected String orgUnitLongitude;
	protected String orgUnitPhone;

	public OrganizationUnitDto() { }

	public OrganizationUnitDto(String orgUnitName, String orgName, String orgUnitLocation, String orgUnitLatitude,
			String orgUnitLongitude, String orgUnitPhone) {
		super();
		this.orgUnitName = orgUnitName;
		this.orgName = orgName;
		this.orgUnitLocation = orgUnitLocation;
		this.orgUnitLatitude = orgUnitLatitude;
		this.orgUnitLongitude = orgUnitLongitude;
		this.orgUnitPhone = orgUnitPhone;
	}

	public String getOrgUnitName() {
		return orgUnitName;
	}

	public void setOrgUnitName(String orgUnitName) {
		this.orgUnitName = orgUnitName;
	}

	public String getOrgName() {
		return orgName;
	}

	public void setOrgName(String orgName) {
		this.orgName = orgName;
	}

	public String getOrgUnitLocation() {
		return orgUnitLocation;
	}

	public void setOrgUnitLocation(String orgUnitLocation) {
		this.orgUnitLocation = orgUnitLocation;
	}

	public String getOrgUnitLatitude() {
		return orgUnitLatitude;
	}

	public void setOrgUnitLatitude(String orgUnitLatitude) {
		this.orgUnitLatitude = orgUnitLatitude;
	}

	public String getOrgUnitLongitude() {
		return orgUnitLongitude;
	}

	public void setOrgUnitLongitude(String orgUnitLongitude) {
		this.orgUnitLongitude = orgUnitLongitude;
	}

	public String getOrgUnitPhone() {
		return orgUnitPhone;
	}

	public void setOrgUnitPhone(String orgUnitPhone) {
		this.orgUnitPhone = orgUnitPhone;
	}

	@Override
	public String toString() {
		return "OrganizationUnitDto [orgUnitName=" + orgUnitName + ", orgName=" + orgName + ", orgUnitLocation="
				+ orgUnitLocation + ", orgUnitLatitude=" + orgUnitLatitude + ", orgUnitLongitude=" + orgUnitLongitude
				+ ", orgUnitPhone=" + orgUnitPhone + "]";
	}
	

	
	
}
