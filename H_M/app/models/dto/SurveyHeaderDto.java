package models.dto;

import java.util.Date;

public class SurveyHeaderDto {
	 
	protected int id;
	protected String organizationName; 
	protected String organizationUnitName;
	protected String channel; 
	protected Date  startDate;
	protected Date  endDate;
	protected String surveyName;
	protected String channelId; 
	
	
	public SurveyHeaderDto() {
		super();
	}
	

	public SurveyHeaderDto(int id, String organizationName, String organizationUnitName, String channel, Date startDate,
			Date endDate, String surveyName, String channelId) {
		super();
		this.id = id;
		this.organizationName = organizationName;
		this.organizationUnitName = organizationUnitName;
		this.channel = channel;
		this.startDate = startDate;
		this.endDate = endDate;
		this.surveyName = surveyName;
		this.channelId = channelId;
	}


	public int getId() {
		return id;
	}



	public void setId(int id) {
		this.id = id;
	}



	public String getOrganizationName() {
		return organizationName;
	}
	public void setOrganizationName(String organizationName) {
		this.organizationName = organizationName;
	}
	public String getOrganizationUnitName() {
		return organizationUnitName;
	}
	public void setOrganizationUnitName(String organizationUnitName) {
		this.organizationUnitName = organizationUnitName;
	}
	public String getChannel() {
		return channel;
	}
	public void setChannel(String channel) {
		this.channel = channel;
	}
	public Date getStartDate() {
		return startDate;
	}
	public void setStartDate(Date startDate) {
		this.startDate = startDate;
	}
	public Date getEndDate() {
		return endDate;
	}
	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}



	public String getSurveyName() {
		return surveyName;
	}

	public void setSurveyName(String surveyName) {
		this.surveyName = surveyName;
	}
	

	public String getChannelId() {
		return channelId;
	}


	public void setChannelId(String channelId) {
		this.channelId = channelId;
	}


	@Override
	public String toString() {
		return "SurveyDto [id=" + id + ", organizationName=" + organizationName + ", organizationUnitName="
				+ organizationUnitName + ", channel=" + channel + ", startDate=" + startDate + ", endDate=" + endDate
				+ ", surveyName=" + surveyName + ", channelId=" + channelId + "]";
	}	

}
