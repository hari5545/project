package models.dto;


public class TabDto {

    protected String channelName;
    protected String purpose;
    protected String header;
    protected String organizationName;
    protected String organizationUnitName;
    protected String templateName;
    protected String departmentName;
    protected String role;
    protected boolean accessAcrossUnits;
    protected boolean accessAcrossDepartments;
    protected String channelDisplayName;


    public TabDto() {
        super();
    }

    public String getChannelDisplayName() {
        return channelDisplayName;
    }

    public void setChannelDisplayName(String channelDisplayName) {
        this.channelDisplayName = channelDisplayName;
    }

    public String getChannelName() {
        return channelName;
    }

    public void setChannelName(String channelName) {
        this.channelName = channelName;
    }

    public String getOrganizationName() {
        return organizationName;
    }

    public void setOrganizationName(String organizationName) {
        this.organizationName = organizationName;
    }

    public String getOrganizationUnitName() {
        return organizationUnitName;
    }

    public void setOrganizationUnitName(String organizationUnitName) {
        this.organizationUnitName = organizationUnitName;
    }

    public boolean isAccessAcrossUnits() {
        return accessAcrossUnits;
    }

    public void setAccessAcrossUnits(boolean accessAcrossUnits) {
        this.accessAcrossUnits = accessAcrossUnits;
    }

    public boolean isAccessAcrossDepartments() {
        return accessAcrossDepartments;
    }

    public void setAccessAcrossDepartments(boolean accessAcrossDepartments) {
        this.accessAcrossDepartments = accessAcrossDepartments;
    }

    public String getPurpose() {
        return purpose;
    }

    public void setPurpose(String purpose) {
        this.purpose = purpose;
    }

    public String getHeader() {
        return header;
    }

    public void setHeader(String header) {
        this.header = header;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getTemplateName() {
        return templateName;
    }

    public void setTemplateName(String templateName) {
        this.templateName = templateName;
    }

    public String getDepartmentName() {
        return departmentName;
    }

    public void setDepartmentName(String departmentName) {
        this.departmentName = departmentName;
    }

	@Override
	public String toString() {
		return "TabDto{" +
				"channelName='" + channelName + '\'' +
				", purpose='" + purpose + '\'' +
				", header='" + header + '\'' +
				", organizationName='" + organizationName + '\'' +
				", organizationUnitName='" + organizationUnitName + '\'' +
				", templateName='" + templateName + '\'' +
				", departmentName='" + departmentName + '\'' +
				", role='" + role + '\'' +
				", accessAcrossUnits=" + accessAcrossUnits +
				", accessAcrossDepartments=" + accessAcrossDepartments +
				", channelDisplayName='" + channelDisplayName + '\'' +
				'}';
	}


}
